#include "BumpMapModel.h"



BumpMapModel::BumpMapModel(): m_vertexBuffer(nullptr), m_indexBuffer(nullptr), m_vertexCount(0), m_indexCount(0), m_model(nullptr), m_TextureArray(nullptr)
{
}


